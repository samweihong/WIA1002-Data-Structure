public class PastMedicalRecord {
    private int height;
    private int weight;
    private String occupation;
    private String diabetic;
    private String heartDisease;
    private String smoking;
    private String drinkAlcohol;

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public String getOccupation() {
        return occupation;
    }

    public void setOccupation(String occupation) {
        this.occupation = occupation;
    }

    public String getDiabetic() {
        return diabetic;
    }

    public void setDiabetic(String diabetic) {
        this.diabetic = diabetic;
    }

    public String getHeartDisease() {
        return heartDisease;
    }

    public void setHeartDisease(String heartDisease) {
        this.heartDisease = heartDisease;
    }

    public String getSmoking() {
        return smoking;
    }

    public void setSmoking(String smoking) {
        this.smoking = smoking;
    }

    public String getDrinkAlcohol() {
        return drinkAlcohol;
    }

    public void setDrinkAlcohol(String drinkAlcohol) {
        this.drinkAlcohol = drinkAlcohol;
    }
}